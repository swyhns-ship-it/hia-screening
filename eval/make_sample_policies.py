def main():
    os.makedirs(OUT, exist_ok=True)
    for name, text in POLICIES.items():
        doc = Document()
        for line in text.strip().split("\n"):
            doc.add_paragraph(line.strip())
        path = os.path.join(OUT, name + ".docx")
        doc.save(path)
        print("写出:", os.path.join("eval", "policies", name + ".docx"))
